void solve() {
  int n;
  cin >> n;
  vector<int> A(n);
  rep(i, n){
    cin >> A[i];
    A[i]--;
  }
  vector<int> range(n);
  int pre = A[0];
  rep(i, n){
    if(pre == A[i])continue;
    range[pre]++;
    pre = A[i];
  }
  range[pre]++;
  int ans = n;
  rep(i, n){
    int num = range[A[i]] -1;
    if(A[0] != A[i])num++;
    if(A[n-1] != A[i])num++;
    chmin(ans, num);
  }
  cout << ans << endl;
}

