void solve() {
  ll n;
  cin >> n;
  int three = 0, two = 0;
  while(n % 3 == 0 && n != 1){
    three++;
    n /= 3;
  }
  while(n % 2 == 0 && n != 1){
    two++;
    n /= 2;
  }
  if(n != 1){
    cout << -1 << endl;
    return;
  }
  if(two > three){
    cout << -1 << endl;
    return;
  }
  int ans = three + (three - two);
  cout << ans << endl;
}

