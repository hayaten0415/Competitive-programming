void solve() {
  ll n;
  ll k;
  cin >> n >> k;
  vector<ll> A(n);
  map<ll, int> mp;
  rep(i, n){
    cin >> A[i];
    A[i] = A[i] % k;
    if(A[i] != 0)A[i] = k - A[i];
    mp[A[i]]++;
  }
  ll ans = 0;
  for(auto [a, b] : mp){
    if(a == 0)continue;
    chmax(ans, (ll)((b - 1) * k + a + 1));
  }
  cout << ans << endl;
}

