using S = ll;
using F = ll;
S op(S a, S b) { return std::max(a, b); }
S e() { return (-1) * (ll)2e18; }
S mapping(F f, S x) { return x + f; }
F composition(F f, F g) { return f + g; }
F id() { return 0; }
S op2(S a, S b) { return std::min(a, b); }
S e2() { return (ll)2e18; }

void solve() {
  int n, m;
  cin >> n >> m;
  vector<ll> A(n);
  vector<int> vl[n], vr[n+1];
  vector<int> le(m), ri(m);
  rep(i, n){
    cin >> A[i];
  }
  lazy_segtree<S, op2, e2, F, mapping, composition, id> seg_min(A);
  rep(i, m){
    cin >> le[i] >> ri[i];
    le[i]--, ri[i]--;
    vl[le[i]].pb(ri[i]);
    vr[ri[i] + 1].pb(le[i]);
    seg_min.apply(le[i], ri[i] + 1, -1);
  }
  ll ans = -1, chk = -1;
  rep(i, n){
    for(auto d : vl[i]){
      seg_min.apply(i, d + 1, 1);
    }
    for(auto d : vr[i]){
      seg_min.apply(d, i, -1);
    }
    ll mx = A[i];
    ll mn = seg_min.all_prod();
    if (ans < mx - mn) {
			ans = mx - mn; chk = i;
		}
  }
  cout << ans << endl;
  vector<int> res;
  rep(j, m) {
		if (le[j] <= chk && chk <= ri[j])continue;
		res.push_back(j+1);
	}
  cout << res.size() << endl;
  for (auto d : res){
    cout << d << " ";
  }
  cout << endl;
}

